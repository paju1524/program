print("백문이 불여일견\n백견이 불여일타") # \n 은 줄바꿈

print("저는'정다운'입니다.")
print('저는"PAJU-dw"입니다.')

# \" \' : 문장 내에서 따옴표(따옴표가 같을때 사용한다)
print("저는\"정다운\"입니다.")
print('저는\'PAJU-dw\'입니다.')

# \\ : 문장 내에서 \
print("C:\\Users\\python workspace")

# \r : 커서를 맨앞으로 이동
print("Red Apple\rPine")

# \b : 백스페이스 (한 글자 삭제) 그냥 한 글자 지우면 된다 굳이 잘 사용 하지 않는다.
print("Redd\b Apple") #사용 하면 유용하긴 하다

# \t : 탭
print("백문이\t불여일견\t백견이\t불여일타")
